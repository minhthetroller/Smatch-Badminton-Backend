/**
 * Remove Vietnamese diacritics from text
 * @example removeVietnameseDiacritics("Cầu Giấy") => "Cau Giay"
 */
export function removeVietnameseDiacritics(text: string): string {
  return text
    .split('')
    .map((char) => VIETNAMESE_DIACRITICS_MAP[char] || char)
    .join('');
}

/**
 * Normalize Vietnamese text for search
 * - Removes diacritics
 * - Converts to lowercase
 * - Trims whitespace
 * - Normalizes multiple spaces to single space
 */
export function normalizeVietnameseText(text: string): string {
  return removeVietnameseDiacritics(text)
    .toLowerCase()
    .trim()
    .replace(/\s+/g, ' ');
}

/**
 * Check if text contains Vietnamese diacritics
 */
export function hasVietnameseDiacritics(text: string): boolean {
  return text.split('').some((char) => char in VIETNAMESE_DIACRITICS_MAP);
}

/**
 * Generate search variants for Vietnamese text
 * Returns both original (lowercase) and unaccented versions
 * @example generateSearchVariants("Cầu Giấy") => ["cầu giấy", "cau giay"]
 */
export function generateSearchVariants(text: string): string[] {
  const original = text.toLowerCase().trim();
  const unaccented = normalizeVietnameseText(text);
  
  // Return unique variants
  if (original === unaccented) {
    return [original];
  }
  return [original, unaccented];
}

